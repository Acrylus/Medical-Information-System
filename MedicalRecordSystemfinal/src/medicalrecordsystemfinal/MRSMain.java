    public final void displayTable(String name, String userType, int table){
        ArrayList<PatientInfo> pat;
        if (table == 1) {
            pat = con.getPatients(name, userType, 0);
            for (PatientInfo p : pat) {
                String data[] = {Integer.toString(p.getId()), p.getName(), p.getDoctor(), p.getNurse()};
                tbl1.addRow(data);
            }
        } else {
            pat = con.getPatients(name, userType, 1);
            for (PatientInfo p : pat) {
                String data[] = {Integer.toString(p.getId()), p.getName(), p.getDoctor(), p.getNurse()};
                tbl2.addRow(data);
            }
        }
    }
